
/*
 * Includes é uma função nativa do JavaScript que verifica se um elemento existe em uma array
 * retornando true ou false.
 */

let array = [1, 2, 3, 4, 5, 6, 7, 8];

// * Element exist in array
let firstElement = array.includes(5); // * Return true

console.log('Element exist =>', firstElement); // * true
