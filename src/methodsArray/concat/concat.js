/*
 * Concat é um método nativo do JavaScript para unir duas array ou mais array em uma.
 */

// * Arrays
let array1 = [1, 2, 3, 4, 5, 6];
let array2 = ['a', 'b', 'c', 'd', 'e', 'f'];

// * Concat tow arrays in one
let arrayConcat = array1.concat(array2);

console.log('arrayConcat =>', arrayConcat);